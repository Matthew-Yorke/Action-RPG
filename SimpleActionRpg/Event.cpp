#include "Event.h"

//***************************************************************************************************************************************************
//
// Method Name: Event
//
// Description:
//  Initialize member variables with the passed in values.
//
//***************************************************************************************************************************************************
Event::Event(int theCoordinateX, int theCoordinateY, int theWidth, int theHeight)
{
   mpArea = new RectangleObject(theCoordinateX,
                                theCoordinateY,
                                theWidth,
                                theHeight);
}

//***************************************************************************************************************************************************
//
// Method Name: ~Event
//
// Description:
//  Clean up allocated memory.
//
//***************************************************************************************************************************************************
Event::~Event()
{
   delete mpArea;
}

//***************************************************************************************************************************************************
//
// Method Name: GetArea
//
// Description:
//  Return the area the event exists at.
//
//***************************************************************************************************************************************************
RectangleObject* Event::GetArea()
{
   return mpArea;
}