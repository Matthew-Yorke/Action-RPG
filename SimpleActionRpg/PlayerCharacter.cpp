PlayerCharacter::PlayerCharacter(Graphics& theGraphics)
{
   mCurrentHealth = mMaxHealth = 10;
   mCurrentMana = mMaxMana = 5;
   mCoordinateX = 0.0F;
   mCoordinateY = 0.0F;
   mpVelocity = new Vector2D(0.0F,
                             0.0F);
   mpSprite = new Sprite(theGraphics,
                         "../Images/TestSprite.png",
                         0,
                         0,
                         32,
                         32);
   mpHitbox = new Rectangle(mCoordinateX + 10,
                            mCoordinateY,
                            12,
                            32);
   mpMeleeWeapon = new MeleeWeapon(theGraphics,
                                   1,
                                   0,
                                   0,
                                   8,
                                   16);
   mpCurrentState = new IdleState();
   mpCurrentState->SetCharacterReference(this);
}

//***************************************************************************************************************************************************
//
// Method Name: ~PlayerCharacter
//
// Description:
//  TODO: Add method description.
//
//***************************************************************************************************************************************************
PlayerCharacter::~PlayerCharacter()
{
   delete mpVelocity;
   delete mpSprite;
}

//************************************************************************************************************************************************
