//
// Arguments:
//  theGraphics - TODO: Add description.
//
// Return:
//  N/A
//
//************************************************************************************************************************************************
void PlayerCharacter::DrawSprite(Graphics& theGraphics)
{
   mpSprite->Draw(theGraphics,
                  static_cast<int>(mCoordinateX),
                  static_cast<int>(mCoordinateY));
}

