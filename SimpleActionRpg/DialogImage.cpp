#include "DialogImage.h"

//***************************************************************************************************************************************************
// Start Public Method Definitions
//***************************************************************************************************************************************************

//***************************************************************************************************************************************************
//
// Method Name: DialogImage
//
// Description:
//  Create a new image with the file path. Save the starting dimensions of the image.
//
//***************************************************************************************************************************************************
DialogImage::DialogImage(Graphics& theGraphics, std::string theFilePath, int theCoordinateX, int theCoordinateY, int theWidth, int theHeight)
{
   mpImage = new Sprite(theGraphics,
                        theFilePath,
                        0,
                        0,
                        theWidth,
                        theHeight);
   mpDimensions = new RectangleObject(theCoordinateX,
                                      theCoordinateY,
                                      theWidth,
                                      theHeight);
}

//***************************************************************************************************************************************************
//
// Method Name: ~DialogImage
//
// Description:
//  Free any allocated memory that is managed by this class.
//
//***************************************************************************************************************************************************
DialogImage::~DialogImage()
{
   delete mpImage;
   mpImage = nullptr;
   delete mpDimensions;
   mpDimensions = nullptr;
}

//***************************************************************************************************************************************************
//
// Method Name: SetCoordinateX
//
// Description:
//  Update the X-Coordinate with the new passed in value.
//
//***************************************************************************************************************************************************
void DialogImage::SetCoordinateX(int theCoordianteX)
{
   mpDimensions->SetCoordinateX(theCoordianteX);
}

//***************************************************************************************************************************************************
//
// Method Name: SetCoordinateY
//
// Description:
//  Update the Y-Coordinate with the new passed in value.
//
//***************************************************************************************************************************************************
void DialogImage::SetCoordinateY(int theCoordianteY)
{
   mpDimensions->SetCoordinateY(theCoordianteY);
}

//***************************************************************************************************************************************************
//
// Method Name: GetImageWidth
//
// Description:
//  Return the width of the image.
//
//***************************************************************************************************************************************************
int DialogImage::GetImageWidth()
{
   return mpDimensions->GetWidth();
}

//***************************************************************************************************************************************************
//
// Method Name: Draw
//
// Description:
//  Call to draw the image at the current x and y coordinates.
//
//***************************************************************************************************************************************************
void DialogImage::Draw(Graphics& theGraphics)
{
   mpImage->Draw(theGraphics,
                 mpDimensions->GetCoordinateX(),
                 mpDimensions->GetCoordinateY());
}

