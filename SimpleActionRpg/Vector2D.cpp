#include <math.h>
#include "Vector2D.h"

//***************************************************************************************************************************************************
// Start Public Method Definitions
//***************************************************************************************************************************************************

//***************************************************************************************************************************************************
//
// Method Name: Vector2D
//
// Description:
//  Set member variables to their default values with the passed in arguments.
//
//***************************************************************************************************************************************************
Vector2D::Vector2D(float theComponentX, float theComponentY)
{
   mComponentX = theComponentX;
   mComponentY = theComponentY;
}

//***************************************************************************************************************************************************
//
// Method Name: SetComponentX
//
// Description:
//  Update the X-Component of the vector using the passed in argument and recalculate the length of the vector.
//
//***************************************************************************************************************************************************
void Vector2D::SetComponentX(float theComponentX)
{
   mComponentX = theComponentX;
   CalculateLength();
}

//***************************************************************************************************************************************************
//
// Method Name: GetComponentX
//
// Description:
//  Returns the current X-Component of the vector.
//
//***************************************************************************************************************************************************
float Vector2D::GetComponentX()
{
   return mComponentX;
}

//***************************************************************************************************************************************************
//
// Method Name: SetComponentY
//
// Description:
//  Update the Y-Component of the vector using the passed in argument and recalculate the length of the vector.
//
//***************************************************************************************************************************************************
void Vector2D::SetComponentY(float theComponentY)
{
   mComponentY = theComponentY;
   CalculateLength();
}

//***************************************************************************************************************************************************
//
// Method Name: GetComponentY
//
// Description:
//  Returns the current Y-Component of the vector.
//
//***************************************************************************************************************************************************
float Vector2D::GetComponentY()
{
   return mComponentY;
}

//***************************************************************************************************************************************************
//
// Method Name: GetLength
//
// Description:
//  Returns the current length of the vector.
//
//***************************************************************************************************************************************************
float Vector2D::GetLength()
{
   return mLength;
}

//***************************************************************************************************************************************************
// End Public Method Definitions
//***************************************************************************************************************************************************

