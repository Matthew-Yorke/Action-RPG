// Start Private Method Definitions
//***************************************************************************************************************************************************

//***************************************************************************************************************************************************
//
// Method Name: CalculateLength
//
// Description:
//  Calculate the length of the vector using the Pythagorean formula using the X-Component and Y-Component.
//
//***************************************************************************************************************************************************
void Vector2D::CalculateLength()
{
   mLength = sqrtf(powf(fabsf(mComponentX), 2.0F) +
                   powf(fabsf(mComponentY), 2.0F));
}

